def levelOrder_brute(root: TreeNode) -> list[list[int]]:
    """
    DFS approach: collect (depth, val) pairs, sort by depth, group.
    Time: O(n log n) — dominated by sort  |  Space: O(n)
    Shows that DFS can solve level-order but BFS is the natural fit.
    """
    if not root:
        return []

    pairs = []   # (depth, val)

    def dfs(node, depth):
        if not node:
            return
        pairs.append((depth, node.val))
        dfs(node.left,  depth + 1)
        dfs(node.right, depth + 1)

    dfs(root, 0)
    pairs.sort()   # sort by depth — O(n log n)

    result = []
    i = 0
    while i < len(pairs):
        d = pairs[i][0]
        level = []
        while i < len(pairs) and pairs[i][0] == d:
            level.append(pairs[i][1])
            i += 1
        result.append(level)
    return result


