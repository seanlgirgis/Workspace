from collections import deque

def levelOrder(root: TreeNode) -> list[list[int]]:
    """
    BFS with level snapshot.
    Time: O(n)  |  Space: O(n) — queue holds at most one full level
    The critical line: level_size = len(queue) BEFORE the inner loop.
    """
    if not root:
        return []

    result = []
    queue = deque([root])

    while queue:
        level_size = len(queue)     # ← snapshot: nodes at THIS level
        level = []

        for _ in range(level_size):
            node = queue.popleft()
            level.append(node.val)

            if node.left:  queue.append(node.left)
            if node.right: queue.append(node.right)

        result.append(level)

    return result


# Tests
root = build_tree([3, 9, 20, None, None, 15, 7])
print("Level order:", levelOrder(root))   # [[3], [9, 20], [15, 7]]

root2 = build_tree([1])
print("Single node:", levelOrder(root2))  # [[1]]

print("Empty:", levelOrder(None))         # []

# Extension: return values in reverse level order (bottom-up)
# Just return result[::-1]
